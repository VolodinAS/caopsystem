    БАЗЫ ДАННЫХ
        caop_ds_patients - список пациентов
        caop_ds_dirlist - список направлений, с которыми приходят пациенты (пациенты могут приходить несколько раз)
    	caop_ds_visits - визиты пациента
        caop_ds_regimens - шаблоны схем лечения
    	caop_ds_dose_measure_type - типы измерения дозировки препарата
        caop_ds_dose_period_type - скорость введения препарата (сут/час/кг)
        caop_ds_freq_period_type - частота приёма препарата (типа, 1 раз в [28] дней/недель/месяцев)
        caop_ds_visit_regimens - схемы, применяемые к определенным визитам
